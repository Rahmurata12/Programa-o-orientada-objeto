package checkpoint2;

public class Aluno {
	
	private String nome;
	private String DtNasc;
	private String RG;
	private String CPF;
	private String Tel;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getDtNasc() {
		return DtNasc;
	}
	public void setDtNasc(String dtNasci) {
		this.DtNasc = dtNasci;
	}
	public String getRG() {
		return RG;
	}
	public void setRG(String rG) {
		RG = rG;
	}
	public String getCPF() {
		return CPF;
	}
	public void setCPF(String cPF) {
		CPF = cPF;
	}
	public String getTel() {
		return Tel;
	}
	public void setTel(String tel) {
		Tel = tel;
	}

}
